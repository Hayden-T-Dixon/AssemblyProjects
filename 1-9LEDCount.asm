LDI R16, 0xFF
LDI R17, 0x00

OUT DDRD, R16
OUT PORTD, R17

SBI DDRE, 5

main:

	LDI R18, 0b00000000
	OUT PORTD, R18
	SBI PORTE, 5
	CALL DELAY

	LDI R18, 0b01101011
	OUT PORTD, R18
	SBI PORTE, 5
	CALL DELAY

	LDI R18, 0b00000000
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b00001000
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b01100010
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b00000000
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b00000110
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b01101000
	OUT PORTD, R18
	SBI PORTE, 5
	CALL DELAY

	LDI R18, 0b00000000
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	LDI R18, 0b01100000
	OUT PORTD, R18
	CBI PORTE, 5
	CALL DELAY

	rjmp main


DELAY:
	LDI R20, 100
	LOOP1:
		LDI R21, 255
		LOOP2:
			LDI R22, 255
			LOOP3:
				DEC R22
				BRNE LOOP3
			DEC R21
			BRNE LOOP2
		DEC R20
		BRNE LOOP1

	RET
