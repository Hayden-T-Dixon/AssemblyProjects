DELAY:
	LDI R20, 100
	LOOP1:
		LDI R21, 255
		LOOP2:
			LDI R22, 255
			LOOP3:
				DEC R22
				BRNE LOOP3
			DEC R21
			BRNE LOOP2
		DEC R20
		BRNE LOOP1

	RET
